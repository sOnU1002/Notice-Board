        <p id="firstPara" > The Computer Engineering Department <br><br><br>

          Department of Computer Engineering is the largest and most research strong department of its kind in Mumbai University. The Department was established in 2014 and currently offers a B.E in Computer Engineering. The department boasts a vibrant student body and a stellar faculty team of qualified and experienced professors. The  Department has developed many state-of-artfully air-conditioned laboratories with more than 200 desktop computers in various fields of Computer Engineering such as High-Performance Computing, Web Technologies, Cloud Computing, Software Engineering etc. thereby providing ample facilities for project development and research. The Department has tie-up with CSI and maintains a close relationship with industries of repute. The department takes immense interest in conducting professional activities such as organizing workshops, seminars and expert lectures to meet the challenges in the IT industry. Our results are constantly on the upward trajectory and the phenomenal growth of the department is attributed to the winning combination of dedicated and experienced faculty, brilliant students and strong administrative support from the institute.
